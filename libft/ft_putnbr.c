#include "includes/libft.h"

void	ft_putnbr(int nb)
{
	long nb2;

	nb2 = nb;
	if (nb2 < 0)
	{
		ft_putchar('-');
		nb2 = nb2 * -1;
	}
	if (nb2 >= 0 && nb2 < 10)
	{
		ft_putchar(nb2 + '0');
	}
	else
	{
		ft_putnbr(nb2 / 10);
		ft_putchar(nb2 % 10 + '0');
	}
}
